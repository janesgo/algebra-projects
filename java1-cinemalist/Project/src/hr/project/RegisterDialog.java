    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (isFormValid()) {
            try {
                if (userRegistrable.register(tfUsername.getText().trim(),
                        tfPassword.getText())) {
                    dispose();
                } else {
                    MessageUtils.showErrorMessage("Registration failed",
                            "Username already exists");
                }
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Registration error",
                        "Registration error");
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

