    private void initValidation() {
        validationFields = Arrays.asList(tfUsername, tfPassword, tfPasswordRepeat);
        errorLabels = Arrays.asList(lblUsernameError, lblPasswordError, lblPasswordRepeatError);
    }

    public boolean isFormValid() {
        boolean valid = true;

        for (int i = 0; i < validationFields.size(); i++) {
            valid &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setText(validationFields.get(i).getText().trim().isEmpty() ? "X" : "");
        }
        if (!tfPassword.getText().isEmpty() && !tfPasswordRepeat.getText().isEmpty()
                && !tfPassword.getText().equals(tfPasswordRepeat.getText())) {
            MessageUtils.showErrorMessage("Password error", "Passwords do not match");
            valid = false;
        }

        return valid;
    }

    private void clearErrors() {
        errorLabels.forEach(el -> el.setText(""));
    }
